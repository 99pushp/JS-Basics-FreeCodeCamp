//single quote

console.log('he is a \'good\' boy');

//double quotes

console.log("he is a \"bad\" boy");

//backslash

console.log("whut \t");
console.log("whut \\t");

//new line

console.log("hello, \n my name is pushp");

//tab

console.log("hello,\tmy name is pushp");

//carriage return
console.log("hello  jsdfjn  djnsfd\rjnsf jhnsfdj sfbnsj\rifdbn sfjnsjifnjs");

//backspace

console.log("\bjsdfnvisdnvusidfvns \b fndvuifsdbnv \b");

//formfeed The form feed character is used to start a new page in a printed document

console.log("\fjsdfnvisdnvusidfvns \f fndvuifsdbnv \f");